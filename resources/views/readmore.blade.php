                <h1>
                    <b>Bazar Buku (11 - 22 Maret 2019)</b>
                </h1>

                                    <div class="marginbot-bottom30"></div>
                    <img src="https://hmjti.akakom.ac.id/uploads/article/201903131021535c8877517466c_cropped.jpg" class="img-responsive" style="width: 100%">

            </div>

            <div class="post-read"><p>Telah Hadir BAZAR BUKU yang telah dilaksanakan oleh HMJ TI STMIK AKAKOM Yogyakarta dengan melakukan penjualan buku tentang Teknologi Informasi, buku yang dijual diperoleh dari beberapa penerbit yang ada di Indonesia. Bazar Buku ini dilaksanakan pada tanggal 11 - 22&nbsp;Maret 2019 pada pukul 09.00 - 15.00, tepat berada didepan perpustakaan STMIK AKAKOM Yogyakarta.&nbsp;</p>
<p>Dengan adanya kegiatan ini diharapkan para mahasiswa dapat mengasah kemampuan dengan membaca, dikarenakan buku-buku yang terjual sangat lengkap bagi seseorang/mahasiswa yang berkecimpung didunia IT, seperti halnya programmer. Selain target mahasiswa dalam penjualan ini, bazar buku ini juga menargetkan siapapun yang ingin membeli, tidak terkecuali dosen STMIK AKAKOM Yogyakarta.</p></div>

